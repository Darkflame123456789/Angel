    <h1> Tarta de Queso al Horno </h1>
    <H3> Descripcion: </H3>
    <p>La tarta de queso al horno es un postre clásico y delicioso que encanta a todos. Su textura cremosa y su sabor suave la convierten en el cierre perfecto para cualquier comida. A continuación, te presento una receta fácil y detallada para preparar una exquisita tarta de queso al horno.

    </p>

    <h3> Ingredientes: </h3>
    <p>para un molde de 22 cm</p>
    <ul>
        <li>200 g de galletas tipo María.</li>
        <li>100 g de mantequilla derretida.</li>
        <li>600 g de queso crema.</li>
        <li>200 g de azúcar.</li>
        <li>200 ml de nata para montar (crema de leche)</li>
        <li>3 huevos grandes.</li>
        <li>1 cucharadita de esencia de vainilla. </li>
        <li>Ralladura de 1 limón (opcional)</li>
        <li>Mermelada de frutos rojos para decorar (opcional) </li>
    </ul>

    <h3> Preparacion: </h3>
    <h4> Paso 1 : preparar la base </h4>
    <ol>
        <li>Precalentar el horno a 180°C (350°F)</li>
        <li>Triturar las galletas María hasta obtener un polvo fino.</li>
        <li>Mezclar las galletas trituradas con la mantequilla derretida hasta obtener una mezcla homogénea.</li>
        <li>Presionar la mezcla de galletas en el fondo de un molde desmontable de 22 cm, formando una base compacta.</li>
        <li>Hornear la base durante 10 minutos y luego dejar enfriar.</li>
    </ol>

    <h4> Paso 2 : Preparar el relleno del queso </h4>
    <ol>
        <li>En un bol grande, batir el queso crema con el azúcar hasta obtener una mezcla suave y sin grumos.</li>
        <li>Añadir la nata para montar y seguir batiendo hasta que se integre completamente. </li>
        <li>Incorporar los huevos uno a uno, batiendo bien después de cada adición.</li>
        <li>Añadir la esencia de vainilla y la ralladura de limón (si se usa) y mezclar bien. </li>

    </ol>

    <h4> Paso 3 : Hornear la tarta </h4>
    <ol>
        <li>Verter la mezcla de queso sobre la base de galletas ya enfriada.</li>
        <li>Hornear la tarta en el horno precalentado a 180°C durante 45-50 minutos, o hasta que el centro esté casi cuajado pero aún ligeramente tembloroso.</li>
        <li>Apagar el horno y dejar la tarta dentro con la puerta entreabierta durante una hora para que se enfríe lentamente y evitar que se agriete.</li>
    </ol>

    <h4> Paso 4 : Enfriar y decorar </h4>
    <ol>
        <li>Retirar la tarta del horno y dejar enfriar a temperatura ambiente.</li>
        <li>Refrigerar la tarta durante al menos 4 horas, o preferiblemente toda la noche, para que tome consistencia. </li>
        <li>Antes de servir, desmoldar la tarta y, si se desea, cubrir con mermelada de frutos rojos. </li>
    </ol>
    <h4>Paso 5 : Servir</h4>
    <ol>
        <li>Cortar la tarta en porciones y servir fría. </li>
        <li>Disfrutar de esta deliciosa tarta de queso al horno.    </li>
    </ol>
