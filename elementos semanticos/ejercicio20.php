        <article>
            <h2>Generos:</h2>

            <p>A continuacion te explicare los diferentes generos de videojuegos.</p>

            <ol>
                <li>
                    <strong>Aventura:</strong>Los juegos de acción son aquellos que se centran en la velocidad de reflejos y la coordinación ojo-mano del jugador. Estos juegos suelen incluir combates, disparos, y situaciones que requieren movimientos rápidos y precisos. Los subgéneros incluyen juegos de plataformas, juegos de lucha, y shoot 'em up.
                    <br>
                    Ejemplo: Super Mario Bros., Street Fighter
                </li>
                <br>
                <li>
                    <strong>Aventura:</strong> Los juegos de aventura suelen centrarse en la exploración, resolución de puzles y narrativa. Los jugadores asumen el papel de un personaje en un mundo ficticio y deben completar misiones para avanzar en la historia.
                    <br>
                    Ejemplo: The Legend of Zelda: Ocarina of Time, Grim Fandango.
                </li>
                <li>
                    <strong> Rol (RPG):</strong> En los juegos de rol, los jugadores asumen los roles de personajes en un mundo ficticio. Estos juegos suelen implicar la personalización de personajes, la toma de decisiones que afectan la historia, y el desarrollo de habilidades a lo largo del tiempo. Los subgéneros incluyen RPGs de acción y RPGs por turnos.
                    <br>
                    Ejemplo: Final Fantasy VII, The Witcher 3.
                </li>
                <br>
                <li>
                    <strong>Simulación:</strong> Los juegos de simulación intentan replicar actividades de la vida real en un formato de juego. Esto puede incluir desde la construcción de ciudades hasta simuladores de vuelo, o incluso la simulación de relaciones interpersonales.
                    <br>
                    Ejemplo: The Sims, SimCity.
                </li>
                <br>
                <li>
                    <strong>Estrategia:</strong> En los juegos de estrategia, los jugadores deben pensar y planificar con cuidado para superar desafíos y cumplir objetivos. Estos juegos a menudo requieren que los jugadores gestionen recursos, unidades o territorios para ganar. Los subgéneros incluyen estrategia en tiempo real (RTS) y estrategia por turnos.
                    <br>
                    Ejemplo: StarCraft, Civilization VI.
                </li>
                <br>
                <li>
                    <strong>Deportes:</strong> Los juegos de deportes simulan la práctica de deportes tradicionales como el fútbol, el baloncesto, el tenis, entre otros. Pueden ser simulaciones realistas o versiones más arcade y simplificadas de los deportes.
                    <br>
                    Ejemplo: FIFA, NBA 2K.

                </li>
                <br>
                <li>
                    <strong>Carreras:</strong> Este género se centra en la competición de velocidad, ya sea en coches, motos o cualquier otro tipo de vehículo. Puede incluir simulaciones realistas o estilos arcade más orientados a la diversión rápida.
                    <br>
                    Ejemplo: Gran Turismo, Mario Kart.
                </li>
                <br>
                <li>
                    <strong>Terror:</strong> Los juegos de terror están diseñados para asustar al jugador y generar tensión. Suelen incluir elementos de supervivencia, escasez de recursos, y atmósferas sombrías. Algunos juegos de terror se mezclan con otros géneros, como la acción o la aventura.
                </li>
                Ejemplo: Resident Evil, Silent Hill.
                <br>
                <br>
                <li>
                    <strong> Puzle:</strong> Los juegos de puzle desafían al jugador a resolver problemas o acertijos. Estos juegos pueden variar desde juegos simples de lógica hasta complejas series de acertijos interconectados.
                </li>
                Ejemplo: Tetris, Portal.
                <br>
                <br>
                <li>
                    <strong>Battle Royale:</strong>Este es un subgénero de juegos de acción en el que un gran número de jugadores compiten hasta que solo queda uno en pie. Estos juegos suelen combinar la exploración, la supervivencia, y la estrategia en un entorno de mundo abierto.
                    <br>
                    Ejemplo: Fortnite, PlayerUnknown's Battlegrounds (PUBG).
                </li>
                <br>
                <li>
                    <strong>Shooter en primera persona (FPS):</strong>En estos juegos, los jugadores ven la acción a través de los ojos del personaje que controlan, generalmente mientras disparan a enemigos. Suelen centrarse en la precisión, la velocidad de reflejos y la táctica en el combate.
                    <br>
                    Ejemplo: Call of Duty, Halo.
                </li>
                <br>
                <li>
                    <strong>Sandbox:</strong>Los juegos sandbox permiten a los jugadores moverse libremente dentro de un mundo abierto sin restricciones estrictas en la forma de jugar. Los jugadores pueden crear, explorar, y realizar actividades a su propio ritmo.
                    <br>
                    Ejemplo: Minecraft, Grand Theft Auto V.
                </li>
                <br>
                <li>
                    <strong>MOBA (Multiplayer Online Battle Arena):</strong> Este es un subgénero de los juegos de estrategia en tiempo real donde equipos de jugadores controlan a héroes únicos que combaten en un campo de batalla. La coordinación en equipo y la estrategia son cruciales.

                    Ejemplo: League of Legends, Dota 2.
                </li>
                <br>
                <li>
                    <strong>Metroidvania:</strong>Este es un subgénero que combina elementos de los juegos de acción y aventura, caracterizado por un diseño de nivel no lineal donde los jugadores desbloquean nuevas áreas a medida que adquieren habilidades o herramientas.
                    <br>
                    Ejemplo: Super Metroid, Hollow Knight.
                </li>
                <br>
                <li>
                    <strong>Ritmo/Música:</strong> Estos juegos están centrados en la música, y los jugadores deben seguir el ritmo o las notas para progresar. Pueden incluir tocar instrumentos virtuales o seguir una coreografía.
                    <br>
                    Ejemplo: Guitar Hero, Dance Dance Revolution.
                </li>
            </ol>
